def nlp_preprocess(text: str, max_words: int = 500) -> str:
    """
    NLP-предобработка текста для TF-IDF векторизатора.

    Выполняет:
    1. Приведение к нижнему регистру
    2. Удаление пунктуации
    3. Удаление стоп-слов
    4. Лемматизацию
    5. Обрезание до max_words слов

    Args:
        text: Исходный текст новости
        max_words: Максимальное количество слов в результате

    Returns:
        Предобработанный текст (леммы через пробел)
    """
    text = re.sub(r"[^\w\s]", " ", text.lower())

    words = [w for w in text.split() if len(w) > 2 and w not in stop_words]

    lemmas = []
    for w in words:
        try:
            normal_forms = morph.normal_forms(w)
            if normal_forms:
                lemma = normal_forms[0]
                if lemma not in stop_words and len(lemma) > 2:
                    lemmas.append(lemma)
        except:
            continue

    lemmas = lemmas[:max_words]

    return " ".join(lemmas)


def preprocess_batch(texts: list, max_words: int = 500) -> list:
    """
    Предобработка списка текстов.

    Args:
        texts: Список текстов
        max_words: Максимальное количество слов в результате

    Returns:
        Список предобработанных текстов
    """
    return [nlp_preprocess(text, max_words) for text in texts]
